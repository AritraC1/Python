# Datatypes and Type

# Integer
a = 10
print("a =", a, "| Type:", type(a))

# Float
b = 3.14
print("b =", b, "| Type:", type(b))

# String
c = "Hello, Python"
print("c =", c, "| Type:", type(c))

# Boolean (True/False)
d = True
print("d =", d, "| Type:", type(d))

# List
e = [1, 2, 3]
print("e =", e, "| Type:", type(e))

# Tuple
f = (1, 2, 3)
print("f =", f, "| Type:", type(f))

# Dictionary
g = {"name": "Alice", "age": 25}
print("g =", g, "| Type:", type(g))

# Set
h = {1, 2, 3}
print("h =", h, "| Type:", type(h))

# NoneType - None is a special constant used to represent the absence of a value. It is not equivalent to 0.
i = None
print("i =", i, "| Type:", type(i))

# Complex
j = 2 + 3j
print("j =", j, "| Type:", type(j))